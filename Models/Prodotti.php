<?php
class Prodotti
{
    public $nome;
    public $prezzo;
    public $categoria;
    public $poster;

    function __construct($_nome, $_prezzo, Categoria $_categoria, $_poster)
    {
        $this->nome->$_nome;
        $this->prezzo->$_prezzo;
        $this->categoria->$_categoria;
        $this->poster->$_poster;
    }
}
